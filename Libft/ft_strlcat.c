#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	length;

	i = ft_strlen(dst);
	j = 0;
	length = ft_strlen(src);
	if (size <= i)
		length += size;
	else
		length += i;
	while (src[j] && (i + 1) < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (length);
}
